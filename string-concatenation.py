# String Concatenation 

# to concatenate, or combine, two strings we can use the "+" operator 

# For Example 
# Merge Variable "a" with Variable "b" into Variable "c"

a = "Hello"
b = "World"
c = a + b 
print(c)

# Output Would be "HelloWorld"

# to add a space between them we add a  " "

a = "Hello"
b = "World"

c = a + " " + b
print(c)

# Output - "Hello World"