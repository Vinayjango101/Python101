# Specifying a type on a variable is done with casting 
# more like enforcing a specific data type on a variable 
#---------------------------------------------------------------------------------------
# INTEGERS CASTING

x = int(1) # x will be 1 
y = int(2.8) # y will be 2 
z = int("3") # z will be 3

#---------------------------------------------------------------------------------------

# FLOATS CASTING 

f = float(1) # f will be 1.0 
g = float(2.8) # g will be 2.8
h = float("3") # h will be 3.0
i = float("4.2") # i will be 4.2

#---------------------------------------------------------------------------------------

# STRINGS CASTING 

t = str("s1") # t will be s1
j = str(2) # j will be 2
l = str(3.0) # l will be 3.0

#---------------------------------------------------------------------------------------